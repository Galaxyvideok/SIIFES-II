#include <stdio.h>
#include <stdlib.h>

typedef struct tipoNo{
    int valor;
    struct tipoNo *esq;
    struct tipoNo *dir;
}TNo;

TNo *raiz; 

//===============================================================
void inicializa(TNo *r){
    r = NULL;
}
//===============================================================
TNo *criaNo(int valor){
    TNo *novo = (TNo *)malloc(sizeof(TNo));
    novo->valor = valor;
    novo->esq = NULL;
    novo->dir = NULL;
    return novo;
}
